def getMoveData(pgnPaths: list, lichessAnalysis: bool = False, startEval: int = 20, startWDL: list = [205, 614, 181]) -> pl.DataFrame:
    """
    This function reads various data points after each move in the given PGN files
    pgnPaths: list
        List containing the paths to analysed PGN files
    lichessAnalysis: bool
        If this is set, the PGNs are assumed to be analysed by Lichess, containing the [%eval ...] tags
        Otherwise I use my own analysis format
    startEval: int
        Evaluation of the starting position
    startWDL: list
        WDL of the starting position
    return -> pl.DataFrame:
        A ploars DataFrame containing all the data
    """
    gameID = 0

    columns = ["GameID", "Position", "Color", "Move", "Ply", "WhiteElo", "BlackElo", "EvalBefore", "EvalAfter", "WinPBefore", "DrawPBefore", "LossPBefore", "WinPAfter", "DrawPAfter", "LossPAfter", "Result"]
    data = dict()
    for c in columns:
        data[c] = list()

    for pgnPath in pgnPaths:
        with open(pgnPath, 'r') as pgn:
            while game := chess.pgn.read_game(pgn):
                if "WhiteElo" not in game.headers or "BlackElo" not in game.headers:
                    print("No Elo")
                    continue

                ply = 0
                wElo = int(game.headers["WhiteElo"])
                bElo = int(game.headers["BlackElo"])
                result = game.headers["Result"]

                if result not in ["1-0", "1/2-1/2", "0-1"]:
                    print(f"Result not found: {result}")
                    continue

                evalBefore = startEval
                wdlBefore = startWDL

                node = game
                while not node.is_end():
                    board = node.board()
                    node = node.variations[0]
                    ply += 1
                    if node.comment:
                        if lichessAnalysis:
                            if not node.eval():
                                print(f'No evaluation found in {game.headers["White"]}-{game.headers["Black"]}, {game.headers["Date"]} on ply {ply}')
                                break
                            cp = int(node.eval().white().score(mate_score=10000))
                            wdl = [0, 0, 0]
                        else:
                            wdl, cp = functions.readComment(node, True, True)
                            if wdl is None:
                                wdl = [0, 0, 0]
                            if cp is None:
                                print(f'No evaluation found in {game.headers["White"]}-{game.headers["Black"]}, {game.headers["Date"]} on ply {ply}')
                        move = node.move
                        data["GameID"].append(gameID)
                        data["Color"].append(board.turn)
                        data["WhiteElo"].append(wElo)
                        data["BlackElo"].append(bElo)
                        data["Result"].append(result)
                        data["Position"].append(board.fen())
                        data["EvalBefore"].append(evalBefore)
                        data["WinPBefore"].append(wdlBefore[0])
                        data["DrawPBefore"].append(wdlBefore[1])
                        data["LossPBefore"].append(wdlBefore[2])
                        data["EvalAfter"].append(cp)
                        data["WinPAfter"].append(wdl[0])
                        data["DrawPAfter"].append(wdl[1])
                        data["LossPAfter"].append(wdl[2])
                        data["Move"].append(move.uci())
                        data["Ply"].append(ply)
                        evalBefore = cp
                        wdlBefore = wdl
                    else:
                        print(f'No comment found in {game.headers["White"]}-{game.headers["Black"]}, {game.headers["Date"]} on ply {ply}')
                gameID += 1
    df = pl.DataFrame(data)
    return df


def getExpectedScoreDropsPerMove(df: pl.DataFrame, xsdWidth: float = 0.1) -> dict:
    """
    This function calculates the number of different expected score drops for each move in the DataFrame
    df: pl.DataFrame
        DataFrame generated by getMoveData
    xsdWidth: float
        Width of the groups for the expected score drop
    return -> dict
        Indexed by expected score drop, containing the number of moves as values
    """
    drops = dict()
    xsFunction = functions.expectedScore
    for row in df.rows(named=True):
        factor = 1 if row["Color"] else -1
        drop = (xsFunction(row["EvalBefore"]) - xsFunction(row["EvalAfter"])) * factor
        drop = (max(0, drop) + xsdWidth/2) // xsdWidth * xsdWidth

        if drop in drops:
            drops[drop] += 1
        else:
            drops[drop] = 1
        
    return drops


def getExpectedScoreDropsPerGame(df: pl.DataFrame, xsdWidth: float = 0.1, xsFunction = functions.expectedScore, p: float = 1) -> dict:
    """
    This function calculates teh average number of expected score drops per move per game
    df: pl.DataFrame
        DataFrame generated by getMoveData
    xsdWidth: float
        Width of the groups for the average expected score drop
    xsFunction:
        The function used to convert the centipawn evaluations into the expected score
    p: float
        The p-value for the generalised mean
    return -> dict
        Indexed by average expected score drop per game, containing the number of games
    """
    gameDrops = dict()
    gameID = None
    gameAvg = [list(), list()]
    for row in df.rows(named=True):
        if gameID is None:
            gameID = row["GameID"]

        if gameID != row["GameID"]:
            gameID = row["GameID"]
            for i in range(2):
                if len(gameAvg[i]) > 0:
                    avgDrop = scipy.stats.pmean(gameAvg[i], p)
                    avgDrop = (avgDrop + xsdWidth/2) // xsdWidth * xsdWidth
                    if avgDrop in gameDrops:
                        gameDrops[avgDrop] += 1
                    else:
                        gameDrops[avgDrop] = 1

                gameAvg = [list(), list()]

        factor = 1 if row["Color"] else -1
        drop = (xsFunction(row["EvalBefore"]) - xsFunction(row["EvalAfter"])) * factor
        drop = max(0, drop)

        index = 1-max(factor, 0)
        gameAvg[index].append(drop)

    for i in range(2):
        if len(gameAvg[i]) > 0:
            avgDrop = scipy.stats.pmean(gameAvg[i], p)
            avgDrop = (avgDrop + xsdWidth/2) // xsdWidth * xsdWidth
            if avgDrop in gameDrops:
                gameDrops[avgDrop] += 1
            else:
                gameDrops[avgDrop] = 1

    return gameDrops


def getGameEvaluationsFromDF(df: pl.DataFrame) -> list:
    """
    This function extracts the evaluation after each move from the DataFrame and splits them by game. 
    Note that the DataFrame must be grouped into the games and the moves in the games must be in the correct order
    return -> list
        A list containing one list for each game, which contains the evaluation after each move
    """
    gameEvaluations = list()
    evals = list()
    gameID = None
    lastPly = 0
    finalEval = None
    
    for row in df.rows(named=True):
        if gameID is None:
            gameID = row["GameID"]

        if gameID != row["GameID"]:
            evals.append(finalEval)
            gameEvaluations.append(evals)
            evals = list()
            gameID = row["GameID"]
            lastPly = 0

        if row["Ply"] != lastPly+1:
            print('Skipped moves', lastPly, row["Ply"])

        lastPly = row["Ply"]
        evals.append(row["EvalBefore"])
        finalEval = row["EvalAfter"]

    evals.append(finalEval)
    gameEvaluations.append(evals)

    return gameEvaluations


def getMeanDensityFromGameEvaluations(gameEvals: list, groupWidth: float = 0.1, expectedScoreFunction = functions.expectedScore, p: float = 1, normalised: float = True) -> dict:
    """
    This gets the density of the mean game expected score loss
    gameEvals: list
        A list containing a list of eavluations for each game
    groupWidth: float
        The means will get grouped together, this determines the size of the individual groups
    expectedScoreFunction:
        The function used to calcualte the expected score from the evaluation
    p: float
        The p-value for the generalised mean
    normalised: bool
        If this is set, the sum of all means will be 1
        Otherwise, it will be the total number of games
    return -> dict
        Dictionary indexed by the mean expected score drop for each game, containing the (relative) number of games with this drop
    """
    density = dict()
    for gameData in gameEvals:
        if len(gameData) < 2:
            continue

        expectedScoreDropsWhite, expectedScoreDropsBlack = getExpectedScoreDropsFromEvaluations(gameData, expectedScoreFunction)

        for xsDrop in [expectedScoreDropsWhite, expectedScoreDropsBlack]:
            mean = scipy.stats.pmean(xsDrop, p)
            mean = float((mean + groupWidth/2) // groupWidth * groupWidth)

            if mean not in density:
                density[mean] = 1
            else:
                density[mean] += 1

    density = dict(sorted(density.items()))

    if normalised:
        return {k: v/sum(list(density.values())) for k, v in density.items()}

    return density


def getExpectedScoreDropsFromEvaluations(evals: list, expectedScoreFunction = functions.expectedScore) -> tuple:
    """
    This function calculates the expected score drops for both colors from a list of evaluations
    evals: list
        The evaluations before each move
    expectedScoreFunction
        The functions to calculate the expected score from an evaluation
    return -> tuple(list(), list())
        The expected score drops for white and black
    """
    expectedScoreDropsWhite = [max(0, expectedScoreFunction(evals[i]) - expectedScoreFunction(evals[i+1])) for i in range(0, len(evals)-1, 2)]
    expectedScoreDropsBlack = [max(0, expectedScoreFunction(-evals[i]) - expectedScoreFunction(-evals[i+1])) for i in range(1, len(evals)-1, 2)]

    return (expectedScoreDropsWhite, expectedScoreDropsBlack)


def getDistributionFromDensity(density: dict) -> dict:
    """
    This calculates the distribution function, given a normalised density as a dict
    """
    return {k: 1-sum(list(density.values())[:i+1]) for i, k in enumerate(list(density.keys()))}


def lichessAccuracy(expectedScoreDrop: float):
    return functions.accuracy(expectedScoreDrop, 0)/100


def gameAccuracy(expectedScoreDrop: float, sigma: float, offset: float) -> float:
    x = np.maximum(expectedScoreDrop - offset, 0)

    return np.exp(-(x)**2/(2*sigma**2))


def fitGameAccuracyToData(distribution: dict, accuracyFunction):
    return scipy.optimize.curve_fit(accuracyFunction, list(distribution.keys()), list(distribution.values()), bounds=(0, 10))


def calculateGameAccuracies(evals: list, expectedScoreFunction = functions.expectedScore, gameAccuracyFunction = lambda x: gameAccuracy(x, 1.5, 0.2), p: float = 1, scale: float = 100) -> tuple:
    """
    This function calculates the accuracies for both colors in a game
    evals: list
        List of centipawn evaluations
    expectedScoreFunction
        The function used to convert the centipawns into an expected score
    gameAccuracyFunction
        The function used to calculate the accuracy of a game
    p: float
        The p-value for the generalised mean
    scale: float
        A value to rescale the accuracy
    return -> tuple
        (whiteAccuracy, blackAccuracy)
    """
    expectedScoreDropsWhite, expectedScoreDropsBlack = getExpectedScoreDropsFromEvaluations(evals, expectedScoreFunction)

    meanWhite = scipy.stats.pmean(expectedScoreDropsWhite, p)
    meanBlack = scipy.stats.pmean(expectedScoreDropsBlack, p)

    return (float(gameAccuracyFunction(meanWhite)) * scale, float(gameAccuracyFunction(meanBlack)) * scale)


def filterDFByRating(minRating: int = 0, maxRating: int = 3000, maxRatingDiff: int = 3000) -> pl.DataFrame:
    """
    This function filters a given polars DataFrame and returns a new DataFrame with the filters applied
    """
    ndf = df.filter(pl.col("WhiteElo") >= minRating,
                    pl.col("WhiteElo") <= maxRating,
                    pl.col("BlackElo") >= minRating,
                    pl.col("WhiteElo") <= maxRating,
                    abs(pl.col("WhiteElo")-pl.col("BlackElo")) <= maxRatingDiff)

    return ndf


