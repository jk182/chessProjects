def getMoveData(pgnPaths: list, lichessAnalysis: bool = False, startEval: int = 20, startWDL: list = [205, 614, 181]) -> pl.DataFrame:
    """
    This function reads various data points after each move in the given PGN files
    pgnPaths: list
        List containing the paths to analysed PGN files
    lichessAnalysis: bool
        If this is set, the PGNs are assumed to be analysed by Lichess, containing the [%eval ...] tags
        Otherwise I use my own analysis format
    startEval: int
        Evaluation of the starting position
    startWDL: list
        WDL of the starting position
    return -> pl.DataFrame:
        A ploars DataFrame containing all the data
    """
    gameID = 0

    columns = ["GameID", "Position", "Color", "Move", "Ply", "WhiteElo", "BlackElo", "EvalBefore", "EvalAfter", "WinPBefore", "DrawPBefore", "LossPBefore", "WinPAfter", "DrawPAfter", "LossPAfter", "Result"]
    data = dict()
    for c in columns:
        data[c] = list()

    for pgnPath in pgnPaths:
        with open(pgnPath, 'r') as pgn:
            while game := chess.pgn.read_game(pgn):
                if "WhiteElo" not in game.headers or "BlackElo" not in game.headers:
                    if "White" in game.headers and "Black" in game.headers and "Date" in game.headers:
                        print(f'No Elo in {game.headers["White"]}-{game.headers["Black"]}, game.headers["Date"]')
                    else:
                        print('No names')
                    continue

                ply = 0
                wElo = int(game.headers["WhiteElo"])
                bElo = int(game.headers["BlackElo"])
                result = game.headers["Result"]

                if result not in ["1-0", "1/2-1/2", "0-1"]:
                    print(f"Result not found: {result}")
                    continue

                evalBefore = startEval
                wdlBefore = startWDL

                node = game
                while not node.is_end():
                    board = node.board()
                    node = node.variations[0]
                    ply += 1
                    if node.comment:
                        if lichessAnalysis:
                            if not node.eval():
                                print(f'No evaluation found in {game.headers["White"]}-{game.headers["Black"]}, {game.headers["Date"]} on ply {ply}')
                                break
                            cp = int(node.eval().white().score(mate_score=10000))
                            wdl = [0, 0, 0]
                        else:
                            wdl, cp = functions.readComment(node, True, True)
                            if wdl is None:
                                wdl = [0, 0, 0]
                            if cp is None:
                                print(f'No evaluation found in {game.headers["White"]}-{game.headers["Black"]}, {game.headers["Date"]} on ply {ply}')
                        move = node.move
                        data["GameID"].append(gameID)
                        data["Color"].append(board.turn)
                        data["WhiteElo"].append(wElo)
                        data["BlackElo"].append(bElo)
                        data["Result"].append(result)
                        data["Position"].append(board.fen())
                        data["EvalBefore"].append(evalBefore)
                        data["WinPBefore"].append(wdlBefore[0])
                        data["DrawPBefore"].append(wdlBefore[1])
                        data["LossPBefore"].append(wdlBefore[2])
                        data["EvalAfter"].append(cp)
                        data["WinPAfter"].append(wdl[0])
                        data["DrawPAfter"].append(wdl[1])
                        data["LossPAfter"].append(wdl[2])
                        data["Move"].append(move.uci())
                        data["Ply"].append(ply)
                        evalBefore = cp
                        wdlBefore = wdl
                    elif not node.is_end():
                        print(f'No comment found in {game.headers["White"]}-{game.headers["Black"]}, {game.headers["Date"]} on ply {ply}')
                gameID += 1
    df = pl.DataFrame(data)
    return df


def getExpectedScoreDropsPerMove(df: pl.DataFrame, xsdWidth: float = 0.1, excludePerfectMoves: bool = False) -> dict:
    """
    This function calculates the number of different expected score drops for each move in the DataFrame
    df: pl.DataFrame
        DataFrame generated by getMoveData
    xsdWidth: float
        Width of the groups for the expected score drop
    excludePerfectMoves: bool
        If this is true, perfect moves (moves with a drop of <= 0) won't be counted
        This is because over 30% of GM moves are perfect and skew the data
    return -> dict
        Indexed by expected score drop, containing the number of moves as values
    """
    drops = dict()
    xsFunction = functions.expectedScore
    for row in df.rows(named=True):
        factor = 1 if row["Color"] else -1
        drop = (xsFunction(row["EvalBefore"]) - xsFunction(row["EvalAfter"])) * factor
        if excludePerfectMoves and drop <= 0:
            continue

        drop = (max(0, drop) + xsdWidth/2) // xsdWidth * xsdWidth

        if drop in drops:
            drops[drop] += 1
        else:
            drops[drop] = 1
        
    return dict(sorted(drops.items()))


def getExpectedScoreDropsPerGame(df: pl.DataFrame, xsdWidth: float = 0.1, xsFunction = functions.expectedScore, p: float = 1) -> dict:
    """
    This function calculates teh average number of expected score drops per move per game
    df: pl.DataFrame
        DataFrame generated by getMoveData
    xsdWidth: float
        Width of the groups for the average expected score drop
    xsFunction:
        The function used to convert the centipawn evaluations into the expected score
    p: float
        The p-value for the generalised mean
    return -> dict
        Indexed by average expected score drop per game, containing the number of games
    """
    gameDrops = dict()
    gameID = None
    gameAvg = [list(), list()]
    for row in df.rows(named=True):
        if gameID is None:
            gameID = row["GameID"]

        if gameID != row["GameID"]:
            gameID = row["GameID"]
            for i in range(2):
                if len(gameAvg[i]) > 0:
                    avgDrop = scipy.stats.pmean(gameAvg[i], p)
                    avgDrop = (avgDrop + xsdWidth/2) // xsdWidth * xsdWidth
                    if avgDrop in gameDrops:
                        gameDrops[avgDrop] += 1
                    else:
                        gameDrops[avgDrop] = 1

                gameAvg = [list(), list()]

        factor = 1 if row["Color"] else -1
        drop = (xsFunction(row["EvalBefore"]) - xsFunction(row["EvalAfter"])) * factor
        drop = max(0, drop)

        index = 1-max(factor, 0)
        gameAvg[index].append(drop)

    for i in range(2):
        if len(gameAvg[i]) > 0:
            avgDrop = scipy.stats.pmean(gameAvg[i], p)
            avgDrop = (avgDrop + xsdWidth/2) // xsdWidth * xsdWidth
            if avgDrop in gameDrops:
                gameDrops[avgDrop] += 1
            else:
                gameDrops[avgDrop] = 1

    return gameDrops


