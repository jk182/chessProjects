def getMeanDensityFromGameEvaluations(gameEvals: list, groupWidth: float = 0.1, meanFunction=calculateGeneralisedMean, normalised: float = True) -> dict:
    """
    This gets the density of the mean game expected score loss
    gameEvals: list
        A list containing a list of eavluations for each game
    groupWidth: float
        The means will get grouped together, this determines the size of the individual groups
    meanFunction:
        The function used to calculate the mean. It has to return a tuple with the white and black mean
    normalised: bool
        If this is set, the sum of all means will be 1
        Otherwise, it will be the total number of games
    return -> dict
        Dictionary indexed by the mean expected score drop for each game, containing the (relative) number of games with this drop
    """
    density = dict()
    for gameData in gameEvals:
        if len(gameData) < 3:
            continue

        for mean in meanFunction(gameData):
            mean = float((mean + groupWidth/2) // groupWidth * groupWidth)

            if mean not in density:
                density[mean] = 1
            else:
                density[mean] += 1

    density = dict(sorted(density.items()))

    if normalised:
        return {k: v/sum(list(density.values())) for k, v in density.items()}

    return density


def getExpectedScoreDropsFromEvaluations(evals: list, expectedScoreFunction = functions.expectedScore) -> tuple:
    """
    This function calculates the expected score drops for both colors from a list of evaluations
    evals: list
        The evaluations before each move
    expectedScoreFunction
        The functions to calculate the expected score from an evaluation
    return -> tuple(list(), list())
        The expected score drops for white and black
    """
    expectedScoreDropsWhite = [max(0, expectedScoreFunction(evals[i]) - expectedScoreFunction(evals[i+1])) for i in range(0, len(evals)-1, 2)]
    expectedScoreDropsBlack = [max(0, expectedScoreFunction(-evals[i]) - expectedScoreFunction(-evals[i+1])) for i in range(1, len(evals)-1, 2)]

    return (expectedScoreDropsWhite, expectedScoreDropsBlack)


def getDistributionFromDensity(density: dict, addZero: bool = False) -> dict:
    """
    This calculates the distribution function, given a normalised density as a dict
    """
    if addZero and 0 not in density:
        density[0] = 0
        density = dict(sorted(density.items()))
    return {k: sum(list(density.values())[:i+1]) for i, k in enumerate(list(density.keys()))}


def lichessAccuracy(expectedScoreDrop: float):
    return functions.accuracy(expectedScoreDrop, 0)/100


def moveAccuracy(expectedScoreDrop: float, k: float, l: float) -> float:
    return 1-beta.cdf(expectedScoreDrop/100, k, l)
    return 100**(k*expectedScoreDrop)
    # return np.minimum(103.1668100711649 * np.exp(k * expectedScoreDrop) - 3.166924740191411, 100) / 100


def mixtureDistribution(expectedScoreDrop: float, a1: float, b1: float, a2: float, b2: float):
    return 1 - (0.5 * beta.cdf(expectedScoreDrop/100, a1, b1) + 0.5 * beta.cdf(expectedScoreDrop/100, a2, b2))


def gameAccuracy(expectedScoreDrop: float, sigma: float, offset: float) -> float:
    x = np.maximum(expectedScoreDrop - offset, 0)

    return np.exp(-(x**2)/(2*sigma**2))


def fitFunctionToDistribution(distribution: dict, function, bounds: tuple = None):
    if bounds is None:
        return scipy.optimize.curve_fit(function, list(distribution.keys()), [1-v for v in distribution.values()])

    return scipy.optimize.curve_fit(function, list(distribution.keys()), [1-v for v in distribution.values()], bounds=bounds)


def calculateGameAccuracies(evals: list, expectedScoreFunction = functions.expectedScore, gameAccuracyFunction = lambda x: gameAccuracy(x, 1.5, 0.2), p: float = 1, scale: float = 100) -> tuple:
    """
    This function calculates the accuracies for both colors in a game
    evals: list
        List of centipawn evaluations
    expectedScoreFunction
        The function used to convert the centipawns into an expected score
    gameAccuracyFunction
        The function used to calculate the accuracy of a game
    p: float
        The p-value for the generalised mean
    scale: float
        A value to rescale the accuracy
    return -> tuple
        (whiteAccuracy, blackAccuracy)
    """
    expectedScoreDropsWhite, expectedScoreDropsBlack = getExpectedScoreDropsFromEvaluations(evals, expectedScoreFunction)

    meanWhite = scipy.stats.pmean(expectedScoreDropsWhite, p)
    meanBlack = scipy.stats.pmean(expectedScoreDropsBlack, p)

    return (float(gameAccuracyFunction(meanWhite)) * scale, float(gameAccuracyFunction(meanBlack)) * scale)


def filterDFByRating(df: pl.DataFrame, minRating: int = 0, maxRating: int = 3000, maxRatingDiff: int = 3000, minPly: int = 0) -> pl.DataFrame:
    """
    This function filters a given polars DataFrame and returns a new DataFrame with the filters applied
    """
    ndf = df.filter(pl.col("WhiteElo") >= minRating,
                    pl.col("WhiteElo") <= maxRating,
                    pl.col("BlackElo") >= minRating,
                    pl.col("WhiteElo") <= maxRating,
                    abs(pl.col("WhiteElo")-pl.col("BlackElo")) <= maxRatingDiff, 
                    pl.col("Ply") >= minPly)

    return ndf


def removeShortGames(df: pl.DataFrame, maxMoves: int, removeDecisiveGames: bool = False) -> pl.DataFrame:
    """
    This function filters out all short games from a data frame
    """
    gameIDs = list()
    gameID = None
    lastPly = 0
    for row in df.rows(named=True):
        if gameID is None:
            gameID = row["GameID"]

        if gameID != row["GameID"]:
            if lastPly <= 2*maxMoves:
                if removeDecisiveGames:
                    gameIDs.append(gameID)
                elif row["Result"] == "1/2-1/2":
                    gameIDs.append(gameID)

            gameID = row["GameID"]
            lastPly = 1

        lastPly += 1

    ndf = df.clone()
    for gameID in gameIDs:
        ndf = ndf.remove(pl.col("GameID") .eq(gameID))

    return ndf


def calculateVolatilityWeightedMean(evaluations: list, expectedScoreFunction=functions.expectedScore, moveAccuracyFunction=None, minWindowSize: int = 2, maxWindowSize: int = 8, nWindows: int = 10, minVolatility: float = 0.5, maxVolatility: float = 12) -> tuple:
    """
    This calculates the volatility weighted mean of the move accuracy (or expected score drops) for both colors
    evaluations: list
        The evaluations of a game
    expectedScoreFunction
        The function used to calculate the expected score
    moveAccuracyFunction
        The function used to calculate the move accuracy
        If no function is given, the expected score drop will be used as move accuracy
    minWindowSize, maxWindowSize, nWindows:
        Minimum and maximimum window size and the number of windows (if this doesn't lead to window sizes outside of the ranges)
    minVolatility, maxVolatility:
        Lower and upper bounds for the volatility
    return -> tuple
        (whiteMean, blackMean)
    """
    expectedScores = [expectedScoreFunction(evaluation) for evaluation in evaluations]
    windowSize = int(len(expectedScores) / nWindows)
    windowSize = min(maxWindowSize, max(minWindowSize, windowSize))

    prefixCount = windowSize - 2
    windows = [expectedScores[:windowSize] for _ in range(prefixCount)]
    windows.extend([expectedScores[i:i+windowSize] for i in range(len(expectedScores) - windowSize + 1)])

    volatilities = [max(minVolatility, min(maxVolatility, statistics.pstdev(window))) for window in windows]

    accuraciesWhite = list()
    accuraciesBlack = list()

    for i in range(len(expectedScores)-1):
        xsBefore = expectedScores[i]
        xsAfter = expectedScores[i+1]
        volatility = volatilities[i]

        if i % 2 == 0:
            xsDrop = max(0, xsBefore - xsAfter)
            if moveAccuracyFunction is not None:
                accuraciesWhite.append((moveAccuracyFunction(xsDrop), volatility))
            else:
                accuraciesWhite.append((xsDrop, volatility))
        else:
            xsDrop = max(0, xsAfter - xsBefore)
            if moveAccuracyFunction is not None:
                accuraciesBlack.append((moveAccuracyFunction(xsDrop), volatility))
            else:
                accuraciesBlack.append((xsDrop, volatility))

    weightedMeanWhite = sum([accuracy * volatility for accuracy, volatility in accuraciesWhite]) / sum([volatility for _, volatility in accuraciesWhite])
    weightedMeanBlack = sum([accuracy * volatility for accuracy, volatility in accuraciesBlack]) / sum([volatility for _, volatility in accuraciesBlack])

    return (weightedMeanWhite, weightedMeanBlack)


